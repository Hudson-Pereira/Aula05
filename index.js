// com funcao
//criar a funcao e chamar ela depois com os valores
function soma(x,y){
  
  return x+y;
};
console.log(soma(4,40));
console.log(soma(87,3));
console.log(soma(7,3));
console.log(soma(8,54));

function div(g,h){
  return g/h;
};

console.log(div(5,2));
console.log(div(33,3));

function sub(a,b){
  return a-b;
};

console.log(sub(10,5));
console.log(sub(546,43));